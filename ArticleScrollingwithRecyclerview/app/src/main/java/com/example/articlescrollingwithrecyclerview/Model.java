package com.example.articlescrollingwithrecyclerview;


public class Model {
    private int mImage;
    private int mInfo;

    public Model(int mImage, int mInfo) {
        this.mImage = mImage;
        this.mInfo = mInfo;
    }

    public int getmInfo() {
        return mInfo;
    }

    public int getmImage() {
        return mImage;
    }

}
